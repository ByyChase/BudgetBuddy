class Budget:

    def __init__(self, Name, Amount, UnSpent, Description, Paycheck_ID, Budget_ID):
        self.Name = Name
        self.Amount = Amount
        self.UnSpent = UnSpent
        self.Description = Description 
        self.Paycheck_ID = Paycheck_ID
        self.Budget_ID = Budget_ID
        self.User_ID = User_ID